        private void txtDate_Leave(object sender, EventArgs e)
        {
            try
            {
                if (VarGeneral.CheckDate(txtDate.Text))
                {
                    txtDate.Text = Convert.ToDateTime(txtDate.Text).ToString("yyyy/MM/dd");
                }
                else
                {
                    txtDate.Text = "";
                }
            }
            catch
