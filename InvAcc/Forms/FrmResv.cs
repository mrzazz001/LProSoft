                            {
                                comboBox_Rooms.Items.Add(listRoms[i].romno);
                            }
                        }
                    }
                    catch
                    {
                    }
                }
            }
            else
            {
                List<T_Rom> listRoms = new List<T_Rom>(db.T_Roms.Select((T_Rom item) => item).ToList());
