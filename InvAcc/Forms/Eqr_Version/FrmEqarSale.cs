        private void Button_Save_Click(object sender, EventArgs e)
        {
            try
            {
                if (!Button_Save.Enabled)
                {
                    return;
                }
                if (State == FormState.Edit && !CanEdit)
                {
                    MessageBox.Show((LangArEn == 0) ? "لا يمكن اتمام هذه العملية .. الرجاء مراجعة صلاحيات المستخدمين !" : "Can not complete this process .. please see the powers of the users!", VarGeneral.ProdectNam, MessageBoxButtons.OK, MessageBoxIcon.Hand);
                    return;
                }
                if (State == FormState.New && !Button_Add.Enabled)
                {
                    MessageBox.Show((LangArEn == 0) ? "لا يمكن اتمام هذه العملية .. الرجاء مراجعة صلاحيات المستخدمين !" : "Can not complete this process .. please see the powers of the users!", VarGeneral.ProdectNam, MessageBoxButtons.OK, MessageBoxIcon.Hand);
                    return;
                }
                textBox_ID.Focus();
                if (textBox_ID.Text == "")
                {
                    MessageBox.Show((LangArEn == 0) ? "لايمكن ان يكون الرمز فارغا\u0651" : "Can not be a number or name is empty", VarGeneral.ProdectNam, MessageBoxButtons.OK, MessageBoxIcon.Hand);
                    return;
                }
                if (txt_EqarNo.Text == "" || string.IsNullOrEmpty(txt_EqarNo.Tag.ToString()))
                {
                    MessageBox.Show((LangArEn == 0) ? "تأكد من صحة رقم العقار" : "Can not be a Real Estate is empty", VarGeneral.ProdectNam, MessageBoxButtons.OK, MessageBoxIcon.Hand);
                    txt_EqarNo.Focus();
                    return;
                }
                if (txtAinNo.Text == "" || string.IsNullOrEmpty(txtAinNo.Tag.ToString()))
                {
                    T_EqarsData q = db.StockEqarData(int.Parse(txt_EqarNo.Tag.ToString()));
                    for (int i = 0; i < q.T_AinsDatas.Count; i++)
                    {
                        try
                        {
                            if (q.T_AinsDatas[i].AinStatus.Value > 0)
                            {
                                MessageBox.Show((LangArEn == 0) ? "لا يمكن حذف هذا العقار بالكامل لانه يحتوي على عقد او اكثر لاحد عيونه" : "You cannot Delete this property entirely", VarGeneral.ProdectNam);
                                return;
                            }
                        }
                        catch
                        {
                            MessageBox.Show((LangArEn == 0) ? "لا يمكن حذف هذا العقار بالكامل لانه يحتوي على عقد او اكثر لاحد عيونه" : "You cannot Delete this property entirely", VarGeneral.ProdectNam);
                            return;
                        }
                    }
                }
                if (!VarGeneral.CheckDate(txtGDate.Text))
                {
                    txtGDate.Text = n.FormatGreg(VarGeneral.Gdate, "yyyy/MM/dd");
                }
                if (!VarGeneral.CheckDate(txtHDate.Text))
                {
                    txtHDate.Text = n.FormatHijri(VarGeneral.Hdate, "yyyy/MM/dd");
                }
                if (State == FormState.New)
                {
                    GetData();
                    try
                    {
                        db.T_EqarSales.InsertOnSubmit(data_this);
                        db.SubmitChanges();
                    }
                    catch (SqlException ex)
                    {
                        int max = 0;
                        max = db.MaxEqarSaleNo;
                        if (ex.Number != 2627)
                        {
                            return;
                        }
                        MessageBox.Show("الرقم مستخدم من قبل.\n سيتم الحفظ برقم جديد [" + max + "]", VarGeneral.ProdectNam, MessageBoxButtons.OK, MessageBoxIcon.Question);
                        textBox_ID.Text = string.Concat(max);
                        data_this.EqarSaleNo = max.ToString();
                        Button_Save_Click(sender, e);
                    }
                    catch (Exception)
                    {
                        return;
                    }
                }
                else if (State == FormState.Edit)
                {
                    oldData = new T_EqarSale();
                    oldData = data_this;
                    if (data_this.AinID.HasValue)
                    {
                        Eqar_Ain_StatusControl(1, 0, data_this);
                    }
                    else
                    {
                        Eqar_Ain_StatusControl(0, 0, data_this);
                    }
                    GetData();
                    try
                    {
                        db.Log = VarGeneral.DebugLog;
                        db.SubmitChanges(ConflictMode.ContinueOnConflict);
                    }
                    catch (Exception)
                    {
                        if (!string.IsNullOrEmpty(oldData.AinID.Value.ToString()))
                        {
                            Eqar_Ain_StatusControl(1, 2, oldData);
                        }
                        else
                        {
                            Eqar_Ain_StatusControl(0, 2, oldData);
                        }
                        return;
                    }
                }
                if (data_this.AinID.HasValue)
                {
                    Eqar_Ain_StatusControl(1, 2, data_this);
                }
                else
                {
                    Eqar_Ain_StatusControl(0, 2, data_this);
                }
                State = FormState.Saved;
                RefreshPKeys();
                TextBox_Index.TextBox.Text = string.Concat(PKeys.IndexOf(data_this.EqarSaleNo ?? "") + 1);
                SetReadOnly = true;
            }
            catch (Exception error)
            {
                VarGeneral.DebLog.writeLog("Save:", error, enable: true);
                MessageBox.Show(error.Message);
            }
        }
